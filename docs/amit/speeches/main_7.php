		<h3>"Physical-Cyber-Social Computing: An Early 21st Century Approach to Computing for Human Experience"</h3><br>
		<b>Date:</b> 02/10/2016<br> 
		<b>Venue:</b> IET - Ahmedabad University<br> 
		Portrays physical-cyber-social (PCS) computing, which takes ideas from and goes significantly beyond the current progress in cyber-physical systems, socio-technical systems, and cyber-social systems to support computing for human experience (CHE). This talk exemplifes future PCS application scenarios in healthcare and traffic management that are supported by (a) a deeper and richer semantic interdependence and interplay between sensors and devices at physical layers, (b) rich, technology-mediated social interactions, and (c) the gathering and application of collective intelligence characterized by massive and contextually-relevant background knowledge, and advanced reasoning to bridge machine and human perceptions. 

