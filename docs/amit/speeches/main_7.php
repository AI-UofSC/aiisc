		<h3>"Citizen Sensing &amp; Social Media Analysis with Twitris"</h3><br>
		<b>Date:</b> 02/09/2016<br> 
		<b>Venue:</b> IET - Ahmedabad University<br> 
		This talk weaves together three themes/topics: (1) the opportunity to exploit the massive amounts of social data from the participation of millions of users through a wide variety of online social media platforms; (2) the technical challenges and recent research efforts in extracting metadata from casual/informal user-generated content on social media platforms; (3) experiences in building robust, scalable platforms and numerous real-world campaigns/applications (i.e., 2012 US election  tracking/prediction, analysis of 2014 India election, 2014 Jammu and Kashmir floods rescue effort, impact of states' marijuana legalization policies,  prescription drug abuse epidemiology, etc.).

