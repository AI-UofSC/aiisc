<p>
<strong>Description and Purpose of Book Series</strong><br><br>

Computing is ubiquitous and pervasive; it has become an extension of the human being. Today's cars, for example, are equipped with a
series of computers that respond to human commands and environmental conditions to participate in the handling of the vehicle. Proliferating
sensors help with observations, decision making, and sensory modifications. The  emerging semantic Web will lead to machines' being able to
understand data and help humans exploit heterogeneous, multi-source digital media. New applications in
situation monitoring and entertainment are already resulting in the development of experiential environments.
</p>
<p>
The co-editors' experience spans academics, industry,and entrepreneurship. The series will consequently include long-term research
as well as near-term practical technologies serving  university students and professionals, academic researchers, and industry practitioners.
<b>Semantic Web and Beyond: Computing for Human Experience</b> will provide a platform for far-reaching research and semantic technologies that will
shape our world powerfully as computing becomes ever more closely tied to human perception, understanding, and experience.</p>
<br>
<p><strong>Scope</strong></p>
<p>The series presents novel, integrative themes defining some of the most exciting areas in computing, including:</p>
<ul>
	<li>Perception
		<ul>
			<li>Auditory, visual, touch
			<li>Biosubstrate, biosensing, and control; radio-computer-sensor integration to support perception</li>
			<li>Truth, certainty, and possibilities</li>
			<li>Soft computing, perception-based information processing, evolving intelligence</li>
		</ul>
	</li>
	<li>Semantics
		<ul>
			<li>Semantics of information: interoperability and integration</li>
			<li>Multimedia semantics, geospatial semantics</li>
			<li>Semantic Web, ontology-driven systems</li>
			<li>World knowledge: incomplete, uncertain, inconsistent, imperfect</li>
			<li>Humanist computing: modeling and computing with words, concepts, and behavior; human and social factors in knowledge management</li>
		</ul>
	</li>
	<li>Experience
		<ul>
			<li>Cognition,augmented cognition
			<li>Virtual worlds</li>
			<li>Tactile and sensory computing, multi-sensory environments</li>
			</li>
		</ul>
	</li>
	<li>Infrastructure related to perception, semantics, and experience
		<ul>
			<li>Soft computing, modeling and computing with words, modeling behavior and events</li>
			<li>Sensors with associated device and networking issues</li>
			<li>Service oriented architectures/computing: grid computing, P2P computing, Web services</li>
			<li>Metadata and semantic annotation of text, digital media, streams, and multimedia</li>
			<li>Standards that enable technologies related to semantics, perception, and experience</li>
		</ul>
	</li>
	<li>Applications
		<ul>
			<li>Smart home, Digital life; Lifelog&mdash;digital life archive</li>
			<li>Virtual presence and learning</li>
			<li>Business intelligence</li>
			<li>Societal scale informatics</li>
		</ul>
	</li>
</ul>
