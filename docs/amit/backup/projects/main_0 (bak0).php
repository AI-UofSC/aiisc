		<strong>ASD (Active Semantic Documents):</strong>
		<br>
		The LSDIS lab's collaborative research project on Active Semantic Electronic Patient Record with the	<a href="http://www.athensheartcenter.com/">Athens Heart Center</a> (AHC) exemplifies an implementation of ASDs in a healthcare (more specifically cardiology practice) environment. It has so far involved: (1) the development of populated ontologies in the healthcare (specially cardiology practice) domain; (2) the development of an annotation tool that utilizes the developed ontologies for annotation of patient records, and (3) the development of decision support algorithms that support rule and ontology based checking/validation and evaluation. The most important benefit we seek from ASEMR (with its proactive semantic annotations and rule-based evaluation) is (1) the reduction of medical errors that could occur as an oversight, (2) checks such as preferred drug recommendations lead to prescription drug savings for patients leading to improved satisfaction, and (3) assistance in choosing the medically appropriate ICD-9 code could lead to less communications with the insurer and faster payment.
