<h2>2014</h2>
	<p style="text-align:justify">
		<ul>
			<li>
			<strong>Semantic Web-based Data Exchange and Interoperability for OEM-Supplier Collaboration:</strong>
			<br>
			This project will develop a demonstration prototype for OEM-Supplier collaboration for MAI Data Informatics. The web application will have the capability to electronically exchange data and models based on Semantic Web technologies between OEMs and suppliers on an ongoing basis and will be subject to security and access control requirements set up at both sites.  
			<br><br>
			</li>

			<li>
			<strong>NIDA National Early Warning System Network (iN3): An Innovative Approach:</strong>
			<br>
			The goal of this study is to establish an advanced surveillance system for emerging drug use in the U.S. by harmonizing data from web-based sources associated with emerging drug use and a network of sentinel sites for emerging drug use staffed by medical toxicologists.
			<br><br>
			</li>
			
			<li>
			<strong>Trending: Social Media Analysis to Monitor Cannabis and Synthetic Cannabinoid Use (eDrugTrends):</strong>
			<br> 
			The major goals of this project is to develop a comprehensive software platform, eDrugTrends, for semi-automated processing and visualization of the thematic, sentiment, spatio-temporal, and social network dimensions of social media data (Twitter and web forums) on cannabis and synthetic cannabinoid use. The project also aims to deploy eDrugTrends to: (1) identify and compare trends in knowledge, attitudes, and behaviors related to cannabis and synthetic cannabinoid use across U.S. regions with different cannabis legalization policies using Twitter and web forum data, and (b) analyze social network characteristics and identify key influencers (opinions leaders) in cannabis and synthetic cannabinoid-related discussions on Twitter. 
			</li>
			<br><br><br>
		</ul>
	</p>

