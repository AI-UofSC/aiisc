<h2>2013</h2>
	<p style="text-align:justify">
	<strong>A Study of Social Web Data on Buprenorphine Abuse Using Semantic Web Technology:</strong>
		<br>
		Research involving semantic web and NLP techniques for developing ability to aggregate and analyze Web-based forums about burprenorphine/naloxone and buprenorphine abuse practices. A collaboration between epidemiologists at CITAR and Computer Scientists at Kno.e.sis.
		<br><br>
		
		<strong>Towards Commercialization of Twitris&mdash;A System of Collective Intelligence:</strong>
		<br>
		This project plans to further develop a comprehensive platform to compute Collective Social Intelligence from social media data. It currently uses Twitter as a starting point, and brings in news, multimedia, Wikipedia, and Linked Open Data as complementary data/background knowledge for analysis. Its detailed analysis encompasses the spatio-temporal-thematic (where, when, what), people-content-networking (who and how), and emotion-sentiment-intent (perceptions & impact) domains. Given that social media is predominately used as a means of delivering messages, this technology is designed to gain insights and predict trends using data gathered from social media. Additionally, it plans to support transitioning research on advanced social media analysis techniques and early state technology into real world products and services to help improve the quality of information available to decision-makers and their products.
		<br><br>

		<strong>Materials Database Knowledge Discovery and Data Mining:</strong>
		<br>
		With the collaboration of AFRL/RX , the Kno.e.sis Center is introducing the materials and process community to better data management practices. A data exchange system will be created that will allow researchers to index, search, and compare data and will also enable a shortened transition cycle in material science, which usually takes 5 to 10 years. This multi-disciplinary project will develop materials ontologies that establish a common vocabulary and the associated tools to manipulate the structured/unstructured corpus. These ontologies will focused in-depth biomaterials ontology and broader materials research ontology. We will also illustrate the use of the ontology we create to semantically search for relevant KDDM biomaterials information quickly. Datasets and software developed as part of this project will be made available to the broader research community in material science.
		<br><br><br>		
	</p>

