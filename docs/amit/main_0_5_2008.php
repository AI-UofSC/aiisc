<p>
<a href="http://knoesis.wright.edu/amit/">Amit Sheth</a> is an educator, researcher, and entrepreneur. He is the LexisNexis Ohio Eminent Scholar for
Advanced Data Management and Analysis, an endowed
faculty position funded by LexisNexis and the Ohio Board of Regents at Wright State University. He directs the
<a href="http://knoesis.wright.edu/">Kno.e.sis center</a> for Knowledge enabled
Information & Services Science.  The Center and Eminent Scholar's activities are housed on the third floor of the WSU College of Engineering and Computer
Science's new $10 million Joshi Research Center, which is the focal point for statewide research in advanced data management, visualization, bioinformatics,
sensor technologies, and more.  Professor Sheth and Kno.e.sis collaborate closely with
<a href="#daytaohio">daytaOhio</a> on the second floor of the <a href="#joshi">Joshi Research Center</a>.
</p>
<p>
Professor Sheth's  research has led to several commercial products and two companies,  which he founded and managed in various executive roles (COB, CEO, CTO):
Infocosm, with products and services in Enterprise Workflow Management;  and Taalee/Voquette/Semagix, one of the earliest companies with Semantic Web
applications and application development platform. Keys areas of his interests and contributions are: information integration, metadata, workflow management,
Semantic Web, Web and knowledge services, and Services Science. Keys areas of applications of his research and technology development include: life
sciences/biomedicine, health care, defense, intelligence, financial services, telecommunication and GIS.
</p>
<p>
Professor Sheth is an IEEE Fellow and has received recognitions such as the IBM Faculty award. He has published over
250 papers and articles, many of which are highly cited (<a
href="http://www.cs.ucla.edu/~palsberg/h-number.html">h-index</a> = 50; <a
href="http://scholar.google.com/scholar?q=author%3Aa-sheth">22 over 100 citations</a>); given over 190 invited talks
and colloquia, including 30 keynotes; (co)-organized or chaired 40 conferences or workshops; and served on 125 program committees. He is on the
editorial boards of  several journals, including IEEE Internet Computing,
and is Editor-in-Chief of the International Journal on Semantic Web and Information Systems (<a
href="http://www.ijswis.org/">IJSWIS</a>), joint Editor-in-Chief of the Distributed and Parallel Database Journal (<a
href="http://www.springerlink.com/content/100257/">DAPD</a>), and editor of two Springer book series&mdash;<a
href="http://www.springer.com/series/7056">Semantic Web and Beyond</a> (with R. Jain) and <a
href="http://http://www.springer.com/series/5573">Advances in Database Systems </a>(with A. Elmagarmid).
</p>
<p>
During 1994 and 2006, Prof. Sheth was a professor at the University of Georgia. He founded and directed the LSDIS lab at UGA, widely recognized as a
leading international research group in the areas of semantic Web, SOA, and workflows. Earlier, he served in R&D groups at Bellcore, Unisys, and Honeywell.
He received his BE from BITS, Pilani in 1981 and MS and PhD from Ohio State University in 1983 and 1985, respectively.
</p>
